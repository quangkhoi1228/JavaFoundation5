package class03;

public class JavaVariable {
    /**
     * Biến là một đối tượng dùng để lưu trữ dữ liệu
     * trong quá trình chương trình thực thi
     * <p>
     * # Cú pháp khai báo biến
     * ```
     * $dataType $variableName
     * ```
     */


    public static void main(String[] args) {
        System.out.println("Java Variable");


        int duyAge = 18;
        System.out.println(duyAge);

        duyAge = 20;
        System.out.println(duyAge);

        boolean hasChilren = false;
        System.out.println(hasChilren);

        char _duyName = 'D';
        System.out.println(_duyName);


        double $$$ofDuy = 1000000000000000000000000000000.9;
        System.out.println($$$ofDuy);

        char khoiName = 'A';
        System.out.println(khoiName);


        String nameOfKhoi = "K";
        System.out.println(nameOfKhoi);


    }
}
